package com.dsa.day1;

public class ArrayTraversalDemo {
    public static void main(String[] args) {
        int[] arr = {5, 10, 15, 20, 25};

        System.out.println("Array elements:");
        for (int i = 0; i < arr.length; i++) {
            System.out.println("Index " + i + ": " + arr[i]);
        }
    }
}
